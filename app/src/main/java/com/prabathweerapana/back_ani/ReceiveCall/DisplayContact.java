package com.prabathweerapana.back_ani.ReceiveCall;

public class DisplayContact extends ReceiveCall {

    private String name, number,duration;

    public DisplayContact(String name, String number, String duration) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

}


